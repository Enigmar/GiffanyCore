package de.linzn.aiCore.internal;

public class KeywordContainer {
	public int id;
	public int objectID;
	public String keyword;
	public String function;

	public KeywordContainer(int id, int objectID, String keyword, String function) {
		this.id = id;
		this.objectID = objectID;
		this.keyword = keyword;
		this.function = function;
	}

}
